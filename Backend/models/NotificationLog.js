const mongoose = require("mongoose");

const NotificationLogSchema = new mongoose.Schema({

  //////////////////////////////////////////////////////
  // WHO SENT THE MESSAGE
  //////////////////////////////////////////////////////

  senderId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "User",
    required: true
  },

  //////////////////////////////////////////////////////
  // WHO RECEIVED THE MESSAGE
  //////////////////////////////////////////////////////

  receiverId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "User",
    required: true
  },

  //////////////////////////////////////////////////////
  // MESSAGE REFERENCE
  //////////////////////////////////////////////////////

  messageId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "Message",
    required: true
  },

  //////////////////////////////////////////////////////
  // NOTIFICATION TITLE
  //////////////////////////////////////////////////////

  title: {
    type: String,
    default: "New Message"
  },

  //////////////////////////////////////////////////////
  // NOTIFICATION BODY
  //////////////////////////////////////////////////////

  body: {
    type: String,
    required: true
  },

  //////////////////////////////////////////////////////
  // RECEIVER'S FCM TOKEN
  //////////////////////////////////////////////////////

  fcmToken: {
    type: String
  },

  //////////////////////////////////////////////////////
  // STATUS
  //////////////////////////////////////////////////////

  status: {
    type: String,
    enum: [
      "DELIVERED",
      "BLOCKED",
      "FAILED"
    ],
    required: true
  },

  //////////////////////////////////////////////////////
  // WHY BLOCKED
  //////////////////////////////////////////////////////

  blockReason: {
    type: String,
    enum: [
      "MEETING_MODE",
      "USER_MUTED",
      "NONE"
    ],
    default: "NONE"
  },

  //////////////////////////////////////////////////////
  // FIREBASE RESPONSE
  //////////////////////////////////////////////////////

  firebaseResponse: {
    type: String
  },

  //////////////////////////////////////////////////////
  // DELIVERY TIME
  //////////////////////////////////////////////////////

  deliveredAt: {
    type: Date
  }

}, {
  timestamps: true
});

module.exports = mongoose.model(
  "NotificationLog",
  NotificationLogSchema
);